        <h3>Permainan</h3>
        <p>
            <img src="http://cdn.themis-media.com/media/global/images/library/deriv/992/992777.jpg" alt="bullet"><br>
            Menggunakan turn-based rpg sebagai dasarnya, anda akan menghindar dari serangan musuh dengan cara yang unik.<br>
            Apakah ini bagus?<br>
            Pada awal, ini menarik. Karena setiap musuh akan menyerang dengan pola dan tampilan serangan yang berbeda.<br>
            Namun, setelah beberapa kali bertemu dan menyerang, ada sisi repetitif yang mengganggu.<br>
            Repetitif ini diakibatkan oleh pola cara mengalahkan monster yang sama dengan beberapa pilihan berbeda.<br>
        </p>
