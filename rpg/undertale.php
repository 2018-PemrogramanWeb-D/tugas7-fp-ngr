        <p>
            Singkat cerita, seorang anak kecil bernama Frisk jatuh ke dalam gua dan gua tersebut berisikan para monster yang disegel oleh golongan manusia.<br>
            Fokus dalam cerita ini adalah persahabatan dan "sparing".<br>
            Secara singkat, cerita ini kurang bagus dan ada beberapa kekurangan, salah satunya adalah "exposition dumping".<br>
            Karakter yang diberikan unik-unik dan menarik (interaksi dan dialog), akan tetapi, beberapa kurang "character developement" dan disengaja dibuat misterius.<br>
            Game ini juga terdapat "4th wall breaking".<br>
