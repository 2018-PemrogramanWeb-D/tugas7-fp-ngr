        <h3>Cerita</h3>
        <p>
            Untuk permainan ini, ceritanya tidak terlalu serius, namun cukup fun untuk diikuti.<br>
            Yang jelas, karakter-karakter yang anda kamu ketahui tidak terlalu berbeda.<br>
            Karakter-karakter baru sangat fresh dan menambah suatu komedi (meskipun beberapa ada yang tipikal mario luigi clone).<br>

        </p>
        <h3>Permainan</h3>
        <p>
            <img src="https://upload.wikimedia.org/wikipedia/en/0/03/MarioAndLuigiSuperstarSagaGBABattle.png" alt="battle"><br>
            Ini adalah turn-based rpg yang tidak biasa, dimana berhasil/tidaknya serangan bergantung pada input anda.<br>
            Monster akan terlihat dan bisa menyerang anda jika berada dalam rangenya, pada saat bersamaan anda juga bisa menyerangnya dengan palu (didapatkan suatu saat) dan melompat (ala mario bros.)<br>
            Ada 3 jenis awalan sebelum memasuki battle sequence;<br>
            - anda berhasil menyerang dia sebelum musuh menyerang anda > jika palu, musuh anda (yang tidak immune) akan pusing, dan jika melompat, maka jatuh ke musuh (tergantung dengan situasi, bisa menguntungkan, bisa tidak)<br>
            - anda tidak menyerang > battle sequence dengan normal<br>
            - anda diserang (saat musuh menyentuh belakang anda) > anda akan stunned, luigi lebih besar kemungkinan masih stun selama 1-2 ronde<br>
            Anda bermain dengan 2 karakter (Mario dan Luigi), dengan 2 tombol yang berbeda.<br>
            Terlihat membingungkan, tetapi saat bermain tidak akan susah, bahkan unik.<br>
            Anda mempunyai 3 pilihan saat battle sequence;<br>
            SOLO : Menyerang sendiri<br>
            BROS : Menyerang bersama (menghabiskan BP)<br>
            ITEM : Menggunakan item<br>
            RUN : Lari tanpa mendapatkan apa-apa (kabur)<br>
            saat menggunakan BROS attack, anda diharuskan untuk menginput secara konsekutif supaya berhasil, dan ini satu-satunya serangan damage tinggi.<br>
            Saat defence sequence, anda bisa menghindar bahkan counter serangan musuh dengan melompat atau memukul (akan dipilih sendiri)<br><br>

            Saat menjelajah, penggunaan lompat dan palu juga digunakan untuk menyelesaikan puzzle dan lain.<br>
            Menurutku, dengan battle yang dinamis dan penjelajahan yang tetap memerlukan platforming kecil (namun kecil, ala mario bros), sangat menyenangkan<br>
            Dan lebih baik lagi, anda bisa menghindar banyak musuh kecil, karena anda tidak perlu exp yang banyak untuk melawan boss besar (jika anda bisa menghindari seranganya dengan baik).<br>
        </p>
