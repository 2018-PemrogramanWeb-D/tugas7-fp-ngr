        <h3>Permainan</h3>
        <p>
            <img src="https://gematsu.com/wp-content/uploads/2018/07/Taiko-Switch-Rating_07-03-18.jpg" alt="Bermain!"><br>
            Bermain irama dengan drum!<br>
            Siap-siap lengan anda, karena ini bisa menguras banyak tenaga.<br>
            Kontrol untuk bermain ini ada 4 cara, Motion, Touch, Tombol, dan Real (semua nama ini dikarang)<br>
            <h4>Motion</h4>
            <img src="https://gematsu.com/wp-content/uploads/2018/04/Taiko-Drum-Master-Nintendo-Switch-Version_2018_04-19-18_004.jpg" alt="Motion"><br>
            Dengan menggunakan fitur motion dari joycon Nintendo Switch, Andaikan anda memukul drum asli.<br>
            Memukul depan untuk tengah (merah), dan memukul samping untuk luar/rim (biru).<br>
            <h4>Touch</h4>
            <img src="http://www.siliconera.com/wordpress/wp-content/uploads/2018/07/taikoswitch4_thumb.png" alt="Touch"><br>
            Menggunakan touch screen dari Switchny sendiri (portable mode), serasa bermain di touch smartphone.<br>
            Tekan gambar drum untuk tengah (merah), dan luar drum untuk luar/rim (biru).<br>
            <h4>Tombol</h4>
            Menggunakan tombol dalam controller<br>
            <h4>Real</h4>
            <img src="http://cdn.shopify.com/s/files/1/0404/2733/products/IMG_4761_5debe2ca-339d-492d-8868-847a1ce0c816_grande.JPG" alt="Drummin"><br>
            Menggunakan aksesoris berupa drum yang disediakan oleh HORI, seharga Rp 1.300.000 (Sumber : Tokopedia, 11/9/2018 10:49 WIB)<br><br>
            Menggunakan drum adalah terbaik meskipun saya sendiri belum memilikinya (mau beli kapan-kapan), tetapi Motion juga tidak buruk (namun perlu pembiasaan)<br>
            Masalah yang biasa dialami saat Motion adalah terkadang tidak responsif. Ini bisa diakibatkan oleh joycon sendiri.<br>
            Tombol dan Touch responsif, dan cara itu adalah preferensi masing-masing jika tidak ingin memukul.<br><br>
            <img src="https://i0.wp.com/www.play-asia.com/blog/wp-content/uploads/2018/08/Track-Lit.jpg" alt="Track List"><br>
            Terlihat sedikit, namun ada DLC tersedia dalam e-shop.<br>
            Tambahan lain untuk permainan ini adalah party mode untuk 2-4 player.<br>
            Untuk seharga 680-800rb (varietas dari toko-toko), ini cukup memuaskan.<br>
        </p>
