		<p>
            pada tahun 1991, singkat cerita SEGA ingin menandingi console nintendo dan franchise Mario milik mereka dengan Sonic.<br>
            Apakah hasilnya?<br>
            Bermain sebuah permainan platform dengan kecepatan tinggi dengan teknologi saat itu terdengar menarik, tapi apakah sesuai ekspektasi?<br>
        </p>
        <h3>Permainan</h3>
        <p>
            Kontrol untuk bermain ini cukup simpel, 1 tombol dan 1 tombol arah (kanan kiri). Pemain akan mengontrol Sonic (landak biru) yang menggunakan kecepatan sebagai bagian utama dalam permainan ini.<br>
            Memanfaatkan kecepatan dan gravitasi adalah kunci untuk menyelesaikan permainan ini!<br>
            Stage dalam permainan ini berjumlah 6 dan setiap stage ada 3 level yang disebut "ACT" diikuti dengan Final Stage.<br>
            Setiap stage mempunyai tema dan setiap akhir dari stage akan diberikan Boss.<br>
            <img src="http://info.sonicretro.org/images/0/07/Ghzboss.png" alt="Melawan Boss!"><br>
            Awalnya, yang anda lihat saat bermain stage pertama memberi kesan yang sesuai ekspektasi.<br>
            berlari cepat mengejar waktu menuju tujuan. Tapi setelah itu, secara cepat anda sadari, stage selanjutny lebih menuju "tricky platforming",
            dimana anda harus mengatur waktu anda untuk melompat dan menghindari berbagai hal yang berbahaya.<br>
            Terlihat tidak mengganggu, tetapi untuk permainan yang seharusnya "cepat", ini sedikit keluar dari tujuan mereka<br>
            <img src="http://info.sonicretro.org/images/8/82/Marble.png" alt="Lava???"><br>
            <b>Bagaimana dengan level designnya sendiri?</b><br>
            Menarik! Bisa dibilang itu, karena setiap stage mempunyai tantangan tersendiri. Setiap "obstacle" yang dimiliki setiap stage berbeda dengan lain,
             dengan kata lain, tidak repetitif<br>
             Untuk stage biasa sudah bagus, tapi ada stage yang spesial untuk mendapatkan bonus skor! Tapi, lihatlah ini;<br>
             <img src="http://info.sonicretro.org/images/1/1c/Specialstages1.png" alt="Special Stage"><br>
             Jika anda sedikit pusing melihatnya, itu tidak salah anda, karena stage ini memang memusingkan.<br>
             Stage pun dirancang sedemikian rupa agar memanfaatkan gravitasi dan kecepatan dalam hal yang berbeda.<br>
             Area akan berputar selama anda masih dalam stage tersebut dan anda dalam kondisi "lompat".<br>
             Tujuan dalam special stage ini adalah untuk mendapatkan bonus item, Chaos Emerald!<br>
             Meskipun itu, Chaos Emerald tidak lain hanyalah untuk mendapatkan ending terbaik.<br>
             Ada sebuah kristal yang menghalangi anda menuju Chaos Emerald! anda bisa menghilangkannya seperti memecahkan kaca, sedikit demi sedikit akan pecah
             yang akan ditandai dengan warna kristal tersebut.<br>
             Terlihat simpel? Ya! Oleh karena itu, stagenya susah untuk dilakukan dalam 1 waktu main. Tidak hanya karena ada area yang mengirim anda menuju stage selanjutnya (tanpa mendapatkan Chaos Emerald),
             tetapi pemain juga diberi sebuah "Bumper" yang memantulkan Sonic, tombol untuk mempercepat dan memutar arah lain lintasan, dan lain sebagainya.<br>
        </p>
