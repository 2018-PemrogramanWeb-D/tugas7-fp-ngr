        <h3>[Gameplay]</h3>
        <p>
		<br>
        Kontrolnya simpel;<br>
        Tombol arah kanan dan kiri untuk bergerak,<br>
        Tombol (A) untuk lompat, dan arah bawah untuk menyerang ke bawah dan memantul jika mengenai objek atau musuh,<br>
        Tombol (A) dan arah bawah secara bersamaan akan menggunakan special item,<br>
        Tombol (B) untuk menyerang (dengan cangkulnya), dan<br>
        Tombol (C) untuk periksa special item dan lainnya.<br>
        Tombol tersebut bisa diatur sendiri (termasuk start).<br><br>

        Special item akan didapatkan saat menemukan chest besar dalam setiap stage utama (bayar).<br>
        Special item memakan SP yang bisa diupgrade seiring perjalanan anda.<br><br>

        Dalam Stage, anda akan mengumpulkan uang untuk keperluan upgrade dan membeli item.<br>
        Saat anda mati, anda tidak akan kehilangan nyawa (karena memang tidak ada), tetapi sebagai gantinya, anda akan kehilangan beberapa uang.<br>
        Lebih banyak uang yang anda dapat, lebih banyak yang akan hilang.<br><br>

        Anda akan memilih stage dalam World Map, jadi tidak harus urut, tetapi anda harus menyelesaikan stage utama yang ada untuk melewati bagian map selanjutya.<br>
        Setiap stage utama akan memiliki Boss yang unik dan susah.<br>
        Saat anda membuka bagian dalam World Map, perlahan-lahan akan mendapatkan shop dan upgrade.<br>
        Upgrade adalah upgrade limit HP dan SP. Untuk Shop, bisa berupa item healing dan armor.<br><br>

        Catatan yang menarik untuk level designnya, yaitu stage awal akan mengajarkan anda gimmick dalam stage tersebut,<br>
        setelah itu, mereka akan mencoba untuk memberikan implementasi gimmick yang lebih susah untuk menguji anda.<br>
        Design seperti ini sangat saya sukai karena anda tidak perlu mati lebih banyak untuk mempelajari gimmick tersebut.<br><br>

        Kemudian, checkpoint yang anda temukan akan memberi anda bonus uang (tergantung pada uang yang anda miliki sebelumnya) jika memecahkan gelas yang berisi uang tersebut.<br>
        Namun, sebagai gantinya checkpoint tersebut tidak berguna dan jika anda mati, akan kembali ke checkpoint sebelumnya yang tidak dirusak (jika tidak, kembali ke awal stage).<br>
        Dengan ini, anda diberikan leluasa untuk cara bermain.<br><br>
        </p>
